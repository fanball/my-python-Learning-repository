print(bool(18)) #使用bool查看数字18的布尔值，结果为True
print(bool(0))
print(bool(0.0)) #再使用bool查看0和0.0的布尔值，结果为False
print("-------------------")

print(bool("我 勒 个 豆")) #查看字符串的布尔值，结果为True
print(bool("")) #再查看空字符串的布尔值，结果为False
print("-------------------")

print(bool(False))
print(bool(None)) #查看False和None的布尔值，结果都是False，说明它们两个的性质相等